<section id="chapter1" class="block">
<h2 class="block__title">ミッション</h2>

<p class="block__subtitle"></p>
<p class="block__text">霊長類の脳回路を計測・操作し、不安と葛藤の適切なコントロールを目指します<br>
近年、不安障害、強迫性障害やうつ病の治療のために、局所回路の操作による行動変容が注目されています。しかし、不安や意思決定に関わる領野とその機能は、まだ十分にわかっていません。そこで、私は、ヒトと相同の脳構造を持つ霊長類を対象として、情動回路の体系的な研究を行います。特に、不安の定量化に有効な、「接近回避の葛藤課題」を用いて、価値判断に因果的にかかわる神経回路を同定します。さらに、局所回路の操作をすることで、不安や意思決定のコントロールを行います。薬理学的回路操作や多電極記録法といった生理学的手法と、トレーサーウイルスなどの解剖学的手法、fMRIなどの脳画像法、さらに化学遺伝学などの遺伝学的手法を組み合わせ、機能と結合関係を包括的に明らかにしていきます。</p>
</section>

<section id="chapter2" class="block">
<h2 class="block__title">Goals at ASHBi</h2>

<p class="block__text">不安、気分、意欲、あるいは、好き嫌いの価値判断は、⼤脳辺縁系から⼤脳基底核まで脳内に広く散在する回路で情報処理され、⾏動に⼤きな影響を与えています。雨森グループは、神経科学の分野で培ってきた認知・情動に関する脳と行動解析の手法を用いて、ASHBiの目標である<em>「ヒトに付与された特性の理解」</em>と<em>「ヒトに付与された特性の破綻による病態解明」</em>ために、ヒトと相同な大脳辺縁系を有するマカクザルを対象に、その行動・神経メカニズム解析を行います。</p>

<div class="block__content">
<h3 class="block__content__title">研究主題１：　遺伝学的手法を用いたマカクザルの不安回路の機能解明と制御</h3>

<p class="block__content__text">辺縁皮質を起点とする大脳皮質－大脳基底核－ドーパミン回路に焦点を当て、辺縁皮質が不安や気分障害を引き起こすメカニズムを解明します。我々のこれまでの研究から霊長類の辺縁皮質の下流域には、線条体ストリオソーム構造があることが分かってきました。また、近年の齧歯類の研究で、このストリオソームの下流には、黒質ドーパミン細胞があることが明らかになりつつあります。このことから不安障害で見られる罰の過大評価はストリオソームによるDAの活動抑制によって引き起こされる、という仮説を立てています。この仮説を化学遺伝学などの遺伝学的手法と、電気刺激法による経路同定法などを用い、霊長類にて検証し、悲観的な価値判断の生成メカニズムを解明します。</p>
