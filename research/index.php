<div class="block__content">
<h3 class="block__content__title">研究主題２：　霊長類大脳辺縁系の領野間相互作用の機能解明と制御</h3>

<p class="block__content__text">霊長類は、齧歯類と比較して脳のサイズが非常に大きく、その豊富な領野間相互作用は、進化によって獲得されたものの一つと考えられます。ヒトのMRI、MEG、EEG研究が進み、霊長類の脳は<em>領野間の大規模ネットワーク（large-scale network）によって、価値判断・意思決定に関わる高度な情報処理が行われている</em>ことが分かってきました。特に、ヒトやマカクザルの辺縁皮質にはVon Economo Neuron (VEN) という巨大ニューロンや、扁桃体（Amygdala）を中心とした神経振動の同期現象が、辺縁系領野間の信号伝達に重要な役割を果たしているのではないか、と考えられています。我々は、微小電気刺激とfMRIを組み合わせ、相互作用が行われる長距離関連領野を同定し、そこに記録電極を刺入し、神経振動による領野間相互作用を明らかにします。将来的には遺伝子改変マカクザルのAIの領野間相互作用の特徴をEM-BOLD法と神経活動記録法を組み合わせて解明します。</p>
